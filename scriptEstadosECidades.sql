DROP TABLE IF EXISTS TbCidade;
DROP TABLE IF EXISTS TbEstado;

CREATE TABLE IF NOT EXISTS TbEstado (
	cd_estado 	INT NOT NULL PRIMARY KEY,
	nm_estado 	VARCHAR(255) NOT NULL,
	uf_estado 	CHAR(2) NOT NULL,
	nm_capital	VARCHAR(255) NOT NULL,
	nm_regiao	VARCHAR(50) NOT NULL
);

CREATE TABLE IF NOT EXISTS TbCidade (
	cd_id		INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    nm_cidade 	VARCHAR(255) NOT NULL, 
    cd_ibge 	INT,
    cd_estado 	INT NOT NULL,
    is_capital  BOOLEAN,
    FOREIGN KEY (cd_estado) REFERENCES TbEstado(cd_estado)
);

